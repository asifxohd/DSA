""" UNDERFLOW IN STACK  """
'''
In the context of a stack, "underflow" refers to a condition
that occurs when you try to pop (remove) an element from an empty stack. 
In other words, you're attempting to access an element from the stack when 
the stack is already empty. This operation is not allowed, and it leads to an underflow error.
'''

